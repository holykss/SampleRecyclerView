package com.nalive.samplerecyclerview;

public class Item {

    int image;
    String imageTitle;

    public int getImage() {
        return image;
    }

    public String getImageTitle() {
        return imageTitle;
    }

    public Item(int image, String imagetitle) {
        this.image = image;
        this.imageTitle = imagetitle;
    }

}
